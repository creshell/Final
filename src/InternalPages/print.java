      int b = JOptionPane.showConfirmDialog(null, "Are you sure you want to print?");
        if (b == JOptionPane.YES_OPTION) {
            forprint ot = new forprint();

           ot.text.setText("                                             \n\n"
                    + "                                            REPORTS   \n\n\n"
                    
                    + " ID                                    :         " + idData.getText() + "\n\n"
                    + "STUDENT NAME                           :         " + studentname.getText() + "\n\n"
                    + "GENDER                                 :         " + gender.getText() + "\n\n"
                    + "ISBN                                   :         " + ISBN.getText() + "\n\n"
                    + "BOOK NAME                              :         " + bookname.getText() + " \n\n"
                    + "Time In                                :         " + timeIn.getText() + "\n\n"
                    + "Time Out                               :         " + timeOut.getText() + "\n\n");

            ot.setVisible(true);
            this.dispose();
           ot.text.setEditable(true);
        } 
